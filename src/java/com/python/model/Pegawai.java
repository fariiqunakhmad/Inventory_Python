public class Pegawai {
    private String idPegawai, idKota, namaPegawai, alamatPegawai, tlpPegawai;

    /**
     * @return the idPegawai
     */
    public String getIdPegawai() {
        return idPegawai;
    }

    /**
     * @param idPegawai the idPegawai to set
     */
    public void setIdPegawai(String idPegawai) {
        this.idPegawai = idPegawai;
    }

    /**
     * @return the namaPegawai
     */
    public String getNamaPegawai() {
        return namaPegawai;
    }

    /**
     * @param namaPegawai the namaPegawai to set
     */
    public void setNamaPegawai(String namaPegawai) {
        this.namaPegawai = namaPegawai;
    }

    

    /**
     * @return the alamatPegawai
     */
    public String getAlamatPegawai() {
        return alamatPegawai;
    }

    /**
     * @param alamatPegawai the alamatPegawai to set
     */
    public void setAlamatPegawai(String alamatPegawai) {
        this.alamatPegawai = alamatPegawai;
    }

    /**
     * @return the tlpPegawai
     */
    public String getTlpPegawai() {
        return tlpPegawai;
    }

    /**
     * @param tlpPegawai the tlpPegawai to set
     */
    public void setTlpPegawai(String tlpPegawai) {
        this.tlpPegawai = tlpPegawai;
    }

    /**
     * @return the idKota
     */
    public String getIdKota() {
        return idKota;
    }

    /**
     * @param idKota the idKota to set
     */
    public void setIdKota(String idKota) {
        this.idKota = idKota;
    }
    
    
}
